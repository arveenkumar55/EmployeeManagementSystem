package application.model;

public class Team {

	String name;
	String [] list;
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the list
	 */
	public String[] getList() {
		return list;
	}
	/**
	 * @param list the list to set
	 */
	public void setList(String[] list) {
		this.list = list;
	}
	
		

}
	

